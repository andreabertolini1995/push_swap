#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*first;
	t_list	*tmp;
	t_list	*useless;

	useless = NULL;
	first = NULL;
	while (lst != NULL)
	{
		tmp = ft_lstnew((*f)(lst->content));
		ft_lstadd_back(&first, tmp);
		lst = lst->next;
	}
	(*del)(useless);
	return (first);
}
