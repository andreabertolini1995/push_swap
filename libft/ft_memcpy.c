#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*destptr;
	char	*srcptr;

	destptr = (char *) dest;
	srcptr = (char *) src;
	if (destptr == NULL && srcptr == NULL)
		return (NULL);
	i = 0;
	while (i < n)
	{
		destptr[i] = srcptr[i];
		i++;
	}
	return (dest);
}
