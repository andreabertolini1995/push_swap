#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*sptr;

	sptr = (char *) s;
	i = 0;
	while (i < n)
	{
		if (sptr[i] == (char) c)
			return (&sptr[i]);
		i++;
	}
	return (0);
}
